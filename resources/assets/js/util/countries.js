// Convert country code to full name
export const countryCodeToName = ( country, all ) => {
  if ( country !== '' ) {
    for ( var key in all ) {
      if ( key == country ) {
        return all[ key ];
      }
    }
  } else {
    return 'Unknown';
  }
}

// Fallback if region string is empty
export const regionFormat = ( region ) => {
  if ( region !== '' ) {
    return region;
  } else {
    return 'Unknown';
  }
}
